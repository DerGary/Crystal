#import "UIFont+CRYExtension.h"

@implementation UIFont (CRYExtension)

+(UIFont *)ProximaNovaCondLightWithSize:(CGFloat)size
{
    return [UIFont fontWithName:@"ProximaNovaCond-Light" size:size];
}

+(UIFont *)ProximaNovaCondRegularWithSize:(CGFloat)size
{
    return [UIFont fontWithName:@"ProximaNovaCond-Regular" size:size];
}

+(UIFont *)ProximaNovaCondSemiboldWithSize:(CGFloat)size
{
    return [UIFont fontWithName:@"ProximaNovaCond-Semibold" size:size];
}

@end
